     * @param  \App\Link  $link
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Link $link)
    {
        $this->validator($request->all())->validate();
        $link->update($request->all());
        //Flash Message
        Session::flash('message', 'Link updated successfully!');
        return back();
    }

